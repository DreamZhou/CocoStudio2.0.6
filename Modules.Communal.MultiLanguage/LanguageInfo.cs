    static LanguageInfo()
    {
      if (false)
      {
        try
        {
          foreach (LanguageType type in Enum.GetValues(typeof (LanguageType)))
          {
            if (type == LanguageType.Chinese)
              LanguageOption.WriteChineseXml();
            else
              LanguageOption.WriteLanguageXml(type);
          }
        }
        catch
        {
          return;
        }
      }
      string languagePath = LanguageOption.GetLanguagePath(LanguageOption.CurrentLanguage);
      foreach (FieldInfo field in LanguageOption.TypeLanguageInfo.GetFields())
      {
        string name = field.Name;
        string translateInfo = LanguageOption.GetTranslateInfo(name, languagePath);
        if (name != null)
        {
          if (!string.IsNullOrEmpty(translateInfo))
            field.SetValue((object) LanguageOption.TypeLanguageInfo, (object) translateInfo);
          else
            field.SetValue((object) LanguageOption.TypeLanguageInfo, (object) name);
        }
      }
    }
